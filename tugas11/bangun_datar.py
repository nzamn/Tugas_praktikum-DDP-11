# Luas dan Keliling segitiga
def luas_segitiga(a, t):
    luas = 0.5 * a * t
    print("Luas segitiga", 0.5, "*", a, "*", t, "=", luas)
def keliling_segitiga(a, b, c):
    keliling = 10 + 10 + 10
    print("Keliling segitiga", a, "+", b, "+", c, "=", keliling)
 
# Luas dan Keliling persegi
def luas_persegi(s):
    luas =  s * s
    print("Luas persegi", s, "*", s, "=", luas)
def keliling_persegi(s):
    keliling = 4 * s
    print("Keliling persegi", 4, "*", s, "=", keliling)
 
